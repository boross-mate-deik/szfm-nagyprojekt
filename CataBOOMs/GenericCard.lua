function GenericCard(type, name, description, source, multiUse, numOfCasters, numOfEnemies)
    return {
        type = type,
        name = name,
        description = description,
        source = source,
        multiUse = multiUse,
        numOfCasters = numOfCasters,
        numOfEnemies = numOfEnemies
    }
end

return GenericCard